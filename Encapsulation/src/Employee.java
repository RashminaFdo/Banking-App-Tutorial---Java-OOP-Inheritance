public class Employee {
    private int id;
    private String name;



                                 //Getter= gets the data value
                                 //Setter= sets the data value

    //setter of id
    public void setId(int id){
        this.id = id;
    }

    //getter of id
    public  int getId() {
        return id;
    }

    //getter of name
    public String getName() {
        return name;
    }

    //setter of name
    public void setName(String name) {
        this.name = name;
    }
}
