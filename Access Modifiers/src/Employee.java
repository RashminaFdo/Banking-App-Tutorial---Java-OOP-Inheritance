public class Employee {

    int no1;
    int no2;
    int no3;


    public Employee() {
    }

    public Employee(int no1, int no3, int no2) {
        this.no1 = no1;
        this.no3 = no3;
        this.no2 = no2;
    }

    public int getNo1() {
        return no1;
    }

    public void setNo1(int no1) {
        this.no1 = no1;
    }

    public int getNo2() {
        return no2;
    }

    public void setNo2(int no2) {
        this.no2 = no2;
    }

    public int getNo3() {
        return no3;
    }

    public void setNo3(int no3) {
        this.no3 = no3;
    }
}
